parser.add_argument('--config', type=str, help='Configuration file (generated by select_model.py or set by hand)')
parser.add_argument('--exact_epochs', action='store_true', help='Use exact number of training iterations (regardless of dataset size)')
parser.add_argument('--override_tr_data', type=str, help='xxx', default=None)

args = parser.parse_args()

if __name__ == '__main__':
    os.environ["MXNET_STORAGE_FALLBACK_LOG_VERBOSE"] = "0"
    train_bow_vae(args)

